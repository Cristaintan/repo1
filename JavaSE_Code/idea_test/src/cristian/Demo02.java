package cristian;

public class Demo02 {
    public static void main(String[] args) {
//      定义byte类型的变量
        byte b = 10;
        System.out.println(b);

//       定义short类型的变量
//       定义short类型的变量
        long l = 12345678905555l;   //防止整数过大
        System.out.println(l);
        float f = 13.14f;//防止不兼容
        System.out.println(f);
//  1.名字不能重复
//  2.变量未赋值不能使用
//  3.long类型的变量定义的时候后面加f，防止整数过大
//  4.float类型的变量防止不兼容后面加f

    }
}
